package javaeepp.mihuyou.entity;

public class Star {
    int StarCount;
    int StarGoodsId;
    String StarDate;
    String StarUId;

    public int getStarCount() {
        return StarCount;
    }

    public void setStarCount(int starCount) {
        StarCount = starCount;
    }

    public int getStarGoodsId() {
        return StarGoodsId;
    }

    public void setStarGoodsId(int starGoodsId) {
        StarGoodsId = starGoodsId;
    }

    public String getStarDate() {
        return StarDate;
    }

    public void setStarDate(String starDate) {
        StarDate = starDate;
    }

    public String getStarUId() {
        return StarUId;
    }

    public void setStarUId(String starUId) {
        StarUId = starUId;
    }
}
